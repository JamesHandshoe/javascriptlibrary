var isOn = true;
var isHot = false;

if (isOn) {
	console.log("The light is on");
}

// && is and 
if (isOn && isHot) {
	console.log("Dude, that music is hot!");
}

// || is or

if (isOn || isHot) {
	console.log("That music is on and it is NOT hot");
}

//! is not or opposite
if (!isHot) {
	console.log("it is not hot");
}